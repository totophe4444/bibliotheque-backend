<h4>Données collectées</h4> 
<p>
Les données personnelles collectées et traitées sont celles transmises lors de votre inscription en bibliothèque, notamment vos nom, prénom, civilité, numéro de carte lecteur, email, numéro de téléphone, adresse postale, données de connexions. 
</p>
<h4>Destinataire des données</h4>
<p>
Les données collectées sont transmises aux personnels des bibliothèques de la ville de Paris. Aucun transfert, transmission ou cession de vos données à des tiers non autorisés n’a lieu. Aucune information personnelle autre que celles demandées explicitement n’est collectée à votre insu.
</p>
<h4>Caractère obligatoire ou facultatif de recueil des données</h4> 
<p>
Chaque formulaire limite la collecte des données personnelles au strict nécessaire (minimisation des données).<br>
Le caractère obligatoire des données demandées est symbolisé par un astérisque (*). Ainsi, vous devez impérativement renseigner les catégories de données obligatoires sous peine de ne pas pouvoir transmettre l’ensemble des données aux personnels des bibliothèques. 
</p>
<h4>Durée de conservation des données</h4> 
<p>
Le stockage de ces données est, par ailleurs, limité dans le temps et/ou à votre demande de suppression des données vous concernant. À l’issue de cette période ou de votre demande, vos données personnelles sont supprimées.
</p>
<h4>Sécurité des données</h4> 
<p>
Les données personnelles recueillies dans le cadre des services proposés sur https://bibliotheques.paris.fr/ sont traitées selon des protocoles sécurisés et permettent aux bibliothèques de la ville de Paris de gérer les demandes reçues dans ses applications informatiques. 
</p>
